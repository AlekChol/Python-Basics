'''' 
A program to perform slicing.
'''
word="Holberton"
#get the first three letters
word_first_3=word[0:3]
print("first three letters:{}".format(word_first_3))
#last 2 letters
word_last_2=word[-2:]
print("first three letters:{}".format(word_last_2))
#middle word
middle_word=word[3:-3]
print('middle word:{}'.format(middle_word))